namespace AlohaKit.Controls
{
	/// <summary>
	/// The CaptchaLevel enumeration defines the different levels of complexity for CAPTCHA 
	/// (Completely Automated Public Turing test to tell Computers and Humans Apart) challenges. 
	/// This can be used to configure or determine the strength of a CAPTCHA system, 
	/// balancing between ease of use and security requirements.
	/// </summary>
	public enum CaptchaLevel
	{
		Weak,
		Normal,
		Strong
	}
}